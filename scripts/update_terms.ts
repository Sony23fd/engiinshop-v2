async function main() {
  await prisma.shopSettings.upsert({
    where: { key: 'terms_of_service' },
    update: { value: TERMS_OF_SERVICE },
    create: { key: 'terms_of_service', value: TERMS_OF_SERVICE }
  })
  
  await prisma.shopSettings.upsert({
    where: { key: 'delivery_terms' },
    update: { value: DELIVERY_TERMS },
    create: { key: 'delivery_terms', value: DELIVERY_TERMS }
  })

  await prisma.shopSettings.upsert({
    where: { key: 'privacy_policy' },
    update: { value: PRIVACY_POLICY },
    create: { key: 'privacy_policy', value: PRIVACY_POLICY }
  })
  console.log("Settings successfully updated!")
}

main()
  .catch(e => {
    console.error(e)
    process.exit(1)
  })
  .finally(async () => {
    await prisma.$disconnect()
  })
